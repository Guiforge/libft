#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (&((char*)s)[i]);
		i++;
	}
	if (c == '\0')
		return (&((char*)s)[i]);
	return (NULL);
}

int		ft_indexchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	if (c == '\0')
		return (0);
	return (0);
}
